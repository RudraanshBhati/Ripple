export const Logo = ({ size = 34 }: { size?: number }) => {
  const angles = [0, 60, 120, 180, 240, 300]
  const nodes = angles.map(a => {
    const r = 15, cx = 20, cy = 20
    return [cx + r * Math.cos(a * Math.PI / 180), cy + r * Math.sin(a * Math.PI / 180)]
  })
  return (
    <svg width={size} height={size} viewBox="0 0 40 40">
      <defs>
        <linearGradient id="rg1" x1="0" y1="0" x2="1" y2="1">
          <stop offset="0%" stopColor="#0F3A5F"/>
          <stop offset="100%" stopColor="#1F5F8E"/>
        </linearGradient>
        <linearGradient id="rg2" x1="0" y1="0" x2="1" y2="1">
          <stop offset="0%" stopColor="#2DD4BF"/>
          <stop offset="100%" stopColor="#0EA5A5"/>
        </linearGradient>
      </defs>
      <circle cx="20" cy="20" r="18" fill="none" stroke="url(#rg1)" strokeWidth="1.2"/>
      <circle cx="20" cy="20" r="12" fill="none" stroke="url(#rg2)" strokeWidth="1.2"/>
      <circle cx="20" cy="20" r="6" fill="none" stroke="url(#rg1)" strokeWidth="1.2"/>
      {nodes.map(([x, y], i) => (
        <circle key={i} cx={x} cy={y} r="2" fill={i % 2 === 0 ? '#0F3A5F' : '#2DD4BF'}/>
      ))}
      <polygon points="17,20 19,17 22,17 24,20 22,23 19,23" fill="#0F3A5F"/>
    </svg>
  )
}

export const SeverityBar = ({ value }: { value: number }) => {
  const pct = Math.round(value * 100)
  const color = value > 0.7 ? '#DC2626' : value > 0.4 ? '#D97706' : '#16A34A'
  return (
    <div style={{ display: 'flex', alignItems: 'center', gap: 8, width: '100%' }}>
      <div className="sev-bar" style={{ flex: 1 }}>
        <div className="sev-bar-fill" style={{ width: `${pct}%`, background: `linear-gradient(90deg, ${color}, ${color}CC)` }}/>
      </div>
      <span className="mono" style={{ fontSize: 10.5, color: 'var(--fg-2)', minWidth: 28, textAlign: 'right', fontWeight: 500 }}>{value.toFixed(2)}</span>
    </div>
  )
}

