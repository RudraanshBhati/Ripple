export function severityLabel(score: number) {
  if (score >= 0.8) return { label: 'CRITICAL', cls: 'badge-red' }
  if (score >= 0.6) return { label: 'HIGH', cls: 'badge-red' }
  if (score >= 0.35) return { label: 'MEDIUM', cls: 'badge-amber' }
  return { label: 'INFO', cls: 'badge-green' }
}

export function severityDotColor(score: number) {
  if (score >= 0.7) return '#DC2626'
  if (score >= 0.4) return '#D97706'
  return '#16A34A'
}

export function fmtTime(isoStr: string) {
  const d = new Date(isoStr)
  const now = Date.now()
  const diff = now - d.getTime()
  const mins = Math.floor(diff / 60000)
  if (mins < 1) return 'just now'
  if (mins < 60) return `${mins}m ago`
  const hrs = Math.floor(mins / 60)
  if (hrs < 24) return `${hrs}h ago`
  return `${Math.floor(hrs / 24)}d ago`
}

